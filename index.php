        <h2 class="post-title"> Освейское, Витебская область </h2>

        <p> Второе по величине озеро в Беларуси находится на севере Верхнедвинского района Витебской области. При достаточно большой площади водного зеркала (почти 53 км2), средняя глубина Освейского озера не такая уж и большая – почти 2 метра. На водной глади «плавает» величественный остров, которого местные жители прозвали «Призраком». И не потому, что остров время от времени меняет свои координаты: по словам очевидцев, здесь часто виднеется белесоватое пятно, которое распугивает рыбу. И действительно, рыбакам на крупном острове делать нечего: клева не будет. Но «Призрак» — далеко не единственный участок суши на озере. Из-за колебаний уровня воды на водном зеркале островов образовалось достаточно много: на данный момент их более 20 штук. Территория вокруг озера принадлежит одноименному заказнику, который был создан для сохранения уникальной экосистемы и ее обитателей. На данный момент здесь гнездится баклан, белые цапли, благородные лебеди и другие пернатые. </p> 

