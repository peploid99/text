        <h2 class="post-title"> Лукомльское, Витебская область </h2>

        <p> Площадь озера составляет 37,7 км2, глубина водоема в некоторых местах достигает 11,5 метра. Невероятно красивое озеро Витебщины имеет сложно устроенную котловину. Подводная часть озера чередуется мелями и впадинами. Помимо их, есть еще и 4 острова. Вода в озере пресная, теплая и чистая. Последнее качество – заслуга дрейсены (небольшие двустворчатые моллюски, которые обитают здесь в большом количестве и очищают воду буквально за 2 месяца). А температура воды выше нормы потому, что озеро является охладителем для Лукомльской ГРЭС. В толще  воды обитает огромное количество рыб , поэтому здесь хорошо развито любительское рыболовство, а на незамерзающей части озера зимуют утки. Живописная местность и уникальные характеристики водоема создали все условия для развития инфраструктуры: так, на западной стороне озера работает детский лагерь и профилакторий. </p> 

