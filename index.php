        <h2 class="post-title"> Дрисвяты, Витебская область </h2>

        <p> Озеро расположено на белорусско-литовской границе. Его площадь составляет почти 45 км2, длина – 13 км, а максимальная глубина – 33,3 метра. Благодаря низкому содержанию органических веществ в толще воды, ее прозрачность в некоторых местах достигает 4 метров. Озеро находится на реке с одноименным названием, его питают 13 водотоков. Извилистая береговая линия озера образует множество живописных заливов, большая часть которых сконцентрирована в южной части. На одном из островов озера в старину стоял величественный замок, от которого не осталось ничего, кроме земляного укрепления. В деревне Дрисвяты сохранился уникальный памятник архитектуры – церковь Св. Петра и Павла (1908 г.). Долгое время озеро использовалось как водоем-охладитель для Игналинской АЭС, из-за чего флора и фауна претерпела сильные изменения. Однако сейчас деятельность станции приостановлена, благодаря чему озерная экосистема начала интенсивно восстанавливаться. </p> 

