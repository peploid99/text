        <h2 class="post-title"> Нещердо, Витебская область </h2>

        <p> Озеро относится к бассейну Западной Двины, его площадь составляет 27,4 км2, а протяженность береговой линии – более 50 км, что дает право назвать Нещердо рекордсменом в этой категории. Извилистые и сильно изрезанные берега с большим количеством мысов и заливов придают озеру экзотическую живописность. Внешне водоем больше напоминает величественное, умиротворенное и абсолютно спокойное озеро-море. На поверхности есть два небольших острова, его питают мелкие реки и ручейки. Недавно территорию, окружающую реликтовое озеро, объявили заказником местного значения. Природоохранный комплекс был создан с целью сохранения уникальной экосистемы и биологического разнообразия. Местный писатель, Ян Борщевский окружил озеро ореолом таинственности: по его рассказам, в озере живут огромные цмоки (белорусские драконы). И правда, местные говорят, что всякое может привидеться. Нещердо популярно среди туристов благодаря своей красоте и возможностями для уединенного отдыха. </p> 

