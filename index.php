        <h2 class="post-title"> Выгонощанское, Брестская область
        </h2>

        <p> Одно из крупнейших озер Брестской области окружено непроходимыми болотами, в которых в годы Великой Отечественной войны скрывались белорусские партизаны. Площадь полесской жемчужины составляет 26 км2, при желании его можно пройти пешком, ведь максимальная глубина водоема – всего 2,7 метра. В далеких 1787 – 1783 годах через озеро был проложен судоходный путь, соединяющий Балтийское и Черное моря. В настоящее время Огинский канал – памятник истории. Выгонощанское озеро – идеальное место для уединенного отдыха, ведь водоем окружают непроходимые болотные тропы, а рядом есть живописная и очень колоритная деревенька со своей историей. Озеро находится в самом сердце одноименного заказника, на территории которого гнездятся редкие виды птиц. Туристы из далекой Великобритании, Германии, Швеции и других стран ежегодно приезжают для того, чтобы посмотреть на гнездовья бородатой Неясыти, орлана-белохвоста и других пернатых. Выгонощанское озеро – один из самых крупных бассейнов Европы, который сохранился почти в первозданном виде. Оно является Ключевой Орнитологической территорией, а по Рамсарской конвенции имеет статус водно-болотного угодья европейского значения. </p> 

