        <h2 class="post-title"> Дривяты, Витебская область </h2>

        <p> На севере Беларуси есть «голубое ожерелье», жемчужиной которого является озеро Дривяты, входящее в группу Браславских озер. История этого водоема начинается еще с ледникового периода. Шестое по величине и, пожалуй, первое по красоте озеро Беларуси объединяет множество водоемов Браславского края в единый водный организм. Площадь зеркальной глади составляет чуть больше 36 км2, есть 6 небольших островов. Подводная часть мелководна: поговаривают, что заядлые рыбаки могут запросто назвать около 50 мелей. Именно поэтому летом вода в Дривятах прогревается очень быстро. Озеро открыто для ветров любых направлений. В пасмурную погоду вода приобретает свинцовый оттенок, как настоящее море. Издавна Дривяты славились рыбным разнообразием, и сейчас в водах озера можно поймать знаменитого угря, которому местные дали звучное прозвище «король голубых нив». А браславского судака знали еще во времена Российской империи: уж больно вкусной была рыбка, выловленная в уникальной экосистеме.</p> 

