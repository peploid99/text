        <h2 class="post-title"> Червоное, Гомельская область </h2>

        <p> Червоное или Князь-Озеро является уникальным природным экспонатом. Площадь водного зеркала составляет около 40 км2. Крупнейшее озеро Полесья раскинулось среди живописных равнин и реликтовых болот. С названием связано множество легенд: по одной из них, озеро получило такое имя из-за рыбы красноперки, которая здесь обитает в большом количестве; другая же легенда гласит, что в древности здесь, на Гомельщине, произошла величайшая битва между богатырями: из-за пролитой крови озеро окрасилось в алый цвет, поэтому и получило название Червоное. Дно устлано сапропелями: это не только полезные удобрения, но  и лечебные грязи, которые по своим целебным качествам не уступают крымским.  Несмотря на большую площадь поверхности, озеро совсем не глубокое – максимально зарегистрированный показатель равен 3,5 метра. У озера есть свои острова, общая площадь которых составляет 6 га. </p> 

