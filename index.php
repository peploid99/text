        <p> Находится в северо-западной части Беларуси и является частью национального парка «Нарочанский». Площадь водной глади составляет 28,3 км2, а протяженность береговой линии – 31,2 км. Средняя глубина достигает почти 4 метров. Дно озера выстлано сапропелем, а его высокие берега являются местом обитания диких водоплавающих птиц. Из озера берет свое начало река с одноименным названием, которая несет свои воды в Неман и Балтийское море. Ученые полагают, что название озера произошло от финского слова syveri — «углубление», «глубокое место в воде». В водах озера водится большое количество рыбы, а прилегающая территория просто создана для уединенного отдыха на лоне природы, чем и пользуются многочисленные туристы из Беларуси и других стран. </p> 

